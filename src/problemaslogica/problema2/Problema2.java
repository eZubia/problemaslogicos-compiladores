package problemaslogica.problema2;

/**
 *Hacer un pseudocodigo que imprima los numeros del 100 al 0, en orden
 *   decreciente.
 * @author Erik David Zubia HernÃ¡dez
 * @since 11/05/15
 * @version 1.0
 */
public class Problema2 {
    
    
    /**
     * Metodo que imprime los numeros del 100 al 0
     */
    public static void imprimirNumerosDecreciente(){
        for (int i = 100; i >= 0; i--) {
            System.out.println(i);
            
        }
    }
}
